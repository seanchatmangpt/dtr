@app.command("list")
def list_templates() -> None:
    """Show all available built-in DTR test templates."""
    table = Table(title="DTR Built-in Templates", show_lines=False)
    table.add_column("Name", style="bold cyan", no_wrap=True)
    table.add_column("Category", style="magenta")
    table.add_column("Description")

    for name, meta in TEMPLATES.items():
        table.add_row(name, meta["category"], meta["description"])

    console.print(table)
    console.print(
        f"\n[bold]Usage:[/bold] dtr template show [cyan]<name>[/cyan]  |  "
        f"dtr template apply [cyan]<name>[/cyan] --class-name [cyan]<ClassName>[/cyan]"
    )


@app.command("show")
def show_template(
    template_name: str = typer.Argument(
        ...,
        help="Name of the template to display (e.g. basic, api, benchmark)",
    ),
) -> None:
    """Display the full Java source code of a template with syntax highlighting."""
    if template_name not in TEMPLATES:
        console.print(f"[red]Unknown template:[/red] {template_name!r}")
        console.print(
            f"Available templates: {', '.join(TEMPLATES)}"
        )
        raise typer.Exit(1)

    meta = TEMPLATES[template_name]
    console.print(
        f"[bold cyan]{template_name}[/bold cyan]  "
        f"[magenta]{meta['category']}[/magenta]  — {meta['description']}\n"
    )
    syntax = Syntax(
        meta["source"].strip(),
        "java",
        theme="monokai",
        line_numbers=True,
    )
    console.print(syntax)


@app.command("apply")
def apply_template(
    template_name: str = typer.Argument(
        ...,
        help="Name of the template to apply (e.g. basic, api, benchmark)",
    ),
    class_name: str = typer.Option(
        ...,
        "--class-name",
        "-c",
        help="Name of the Java test class to create (e.g. MyApiDocTest)",
    ),
    module: str = typer.Option(
        "dtr-integration-test",
        "--module",
        "-m",
        help="Maven module to create the test in",
    ),
    package: str = typer.Option(
        "controllers",
        "--package",
        "-p",
        help="Java package name",
    ),
    project_dir: Path = typer.Option(
        Path.cwd(),
        "--project-dir",
        "-C",
        help="Maven project root directory",
    ),
    force: bool = typer.Option(
        False,
        "--force",
        "-f",
        help="Overwrite existing file without prompting",
    ),
) -> None:
    """Generate a Java test file from a built-in template.

    Examples:

        Apply the basic template:
        $ dtr template apply basic --class-name MyFeatureDocTest

        Apply the API template to a specific module:
        $ dtr template apply api --class-name PetsApiDocTest --module petstore-service

        Apply with custom package and force overwrite:
        $ dtr template apply tutorial --class-name GettingStartedDocTest \\
              --package com.example.docs --force
    """
    if template_name not in TEMPLATES:
        console.print(f"[red]Unknown template:[/red] {template_name!r}")
        console.print(f"Available templates: {', '.join(TEMPLATES)}")
        raise typer.Exit(1)

    if not class_name[0].isupper():
        console.print("[yellow]Warning: Class name should start with uppercase[/yellow]")

    if not class_name.endswith("DocTest") and not class_name.endswith("Test"):
        console.print("[yellow]Warning: Class name should end with 'DocTest' or 'Test'[/yellow]")

    # Build output path
    package_path = package.replace(".", "/")
    test_dir = project_dir / module / "src" / "test" / "java" / package_path
    test_file = test_dir / f"{class_name}.java"

    if test_file.exists() and not force:
        console.print(f"[red]File already exists:[/red] {test_file}")
        console.print("Use --force to overwrite.")
        raise typer.Exit(1)

    title = _class_name_to_title(class_name)
    source = TEMPLATES[template_name]["source"].format(
        package=package,
        class_name=class_name,
        title=title,
    )

    test_dir.mkdir(parents=True, exist_ok=True)
    test_file.write_text(source, encoding="utf-8")

    console.print(f"[green]Created:[/green] {test_file}")
    console.print(f"[cyan]Template:[/cyan] {template_name}")
    console.print(f"[cyan]Package:[/cyan]  {package}")
    console.print(f"[cyan]Module:[/cyan]   {module}")
    console.print(f"\n[bold]Next steps:[/bold]")
    console.print(f"  1. Edit {test_file.name} with your content")
    console.print(f"  2. Run: dtr build --modules {module}")
    console.print(f"  3. Check output: target/docs/test-results/{class_name}.md")
