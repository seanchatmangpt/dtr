"""Template command group for DTR CLI.

Provides `dtr template list`, `dtr template show`, and `dtr template apply`
to browse and apply pre-built DTR test templates.

Usage:
    dtr template list                              # Show all built-in templates
    dtr template show basic                        # Display Java source for 'basic'
    dtr template apply api --class-name ApiDocTest # Generate Java test from template
"""

from __future__ import annotations

import re
from pathlib import Path
from typing import Optional

import typer
from rich.console import Console
from rich.syntax import Syntax
from rich.table import Table

console = Console()
app = typer.Typer(
    help="Browse and apply DTR test templates",
    no_args_is_help=True,
)


