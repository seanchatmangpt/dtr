class TestValidateHtml:
    """Test HTML validation logic."""

    def test_good_html_has_no_errors_or_warnings(self):
        errors, warnings = _validate_html(GOOD_HTML, strict=False)
        assert errors == []
        assert warnings == []

    def test_missing_title_is_an_error(self):
        errors, warnings = _validate_html(HTML_NO_TITLE, strict=False)
        assert any("title" in e.lower() for e in errors)

    def test_missing_body_is_an_error(self):
        errors, warnings = _validate_html(HTML_NO_BODY, strict=False)
        assert any("body" in e.lower() for e in errors)

    def test_no_headings_is_an_error(self):
        errors, warnings = _validate_html(HTML_NO_HEADINGS, strict=False)
        assert any("heading" in e.lower() for e in errors)

    def test_empty_title_produces_warning(self):
        errors, warnings = _validate_html(HTML_EMPTY_TITLE, strict=False)
        assert errors == []
        assert any("empty" in w.lower() or "title" in w.lower() for w in warnings)

    def test_no_h1_produces_warning_not_error(self):
        errors, warnings = _validate_html(HTML_NO_H1, strict=False)
        assert errors == []
        assert any("h1" in w.lower() for w in warnings)

    def test_empty_title_in_strict_mode_is_treated_as_error(self):
        errors, warnings = _validate_html(HTML_EMPTY_TITLE, strict=True)
        # In strict mode we pass warnings up to the caller; validator itself still
        # puts it in warnings — the CLI layer promotes them. Here we confirm the
        # warning is detected.
        assert any("empty" in w.lower() or "title" in w.lower() for w in warnings)


