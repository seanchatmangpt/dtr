class TestSchemaGenerateCommand:
    """Test `dtr schema generate` via CLI end-to-end."""

    def _populate_export_dir(self, base: Path) -> Path:
        """Create a minimal DTR export directory with all three file types."""
        (base / "report.html").write_text(GOOD_HTML, encoding="utf-8")
        (base / "data.json").write_text(GOOD_JSON, encoding="utf-8")
        (base / "guide.md").write_text(GOOD_MD, encoding="utf-8")
        return base

    def test_creates_schema_file_by_default(self, tmp_path: Path):
        self._populate_export_dir(tmp_path)

        result = runner.invoke(app, ["schema", "generate", str(tmp_path)])

        assert result.exit_code == 0, result.stdout
        schema_file = tmp_path / ".dtr-schema.json"
        assert schema_file.exists(), ".dtr-schema.json was not created"

    def test_schema_is_valid_json(self, tmp_path: Path):
        self._populate_export_dir(tmp_path)
        runner.invoke(app, ["schema", "generate", str(tmp_path)])
        schema_file = tmp_path / ".dtr-schema.json"
        data = json.loads(schema_file.read_text(encoding="utf-8"))
        assert isinstance(data, dict)

    def test_schema_contains_expected_top_level_keys(self, tmp_path: Path):
        self._populate_export_dir(tmp_path)
        runner.invoke(app, ["schema", "generate", str(tmp_path)])
        data = json.loads((tmp_path / ".dtr-schema.json").read_text())
        assert "$schema" in data
        assert "file_counts" in data
        assert "html_structure" in data
        assert "json_structure" in data
        assert "markdown_structure" in data

    def test_schema_file_counts_are_correct(self, tmp_path: Path):
        self._populate_export_dir(tmp_path)
        runner.invoke(app, ["schema", "generate", str(tmp_path)])
        data = json.loads((tmp_path / ".dtr-schema.json").read_text())
        assert data["file_counts"]["html"] == 1
        assert data["file_counts"]["json"] == 1
        assert data["file_counts"]["markdown"] == 1

    def test_schema_html_structure_has_file_entry(self, tmp_path: Path):
        self._populate_export_dir(tmp_path)
        runner.invoke(app, ["schema", "generate", str(tmp_path)])
        data = json.loads((tmp_path / ".dtr-schema.json").read_text())
        html_entries = data["html_structure"]
        assert len(html_entries) == 1
        assert html_entries[0]["file"] == "report.html"
        assert html_entries[0]["title"] == "DTR Report"

    def test_schema_json_structure_has_section_types(self, tmp_path: Path):
        self._populate_export_dir(tmp_path)
        runner.invoke(app, ["schema", "generate", str(tmp_path)])
        data = json.loads((tmp_path / ".dtr-schema.json").read_text())
        json_entries = data["json_structure"]
        assert len(json_entries) == 1
        assert "paragraph" in json_entries[0]["section_types"]

    def test_schema_includes_observed_section_types(self, tmp_path: Path):
        self._populate_export_dir(tmp_path)
        runner.invoke(app, ["schema", "generate", str(tmp_path)])
        data = json.loads((tmp_path / ".dtr-schema.json").read_text())
        assert "observed_section_types" in data
        assert "paragraph" in data["observed_section_types"]

    def test_schema_markdown_structure_has_heading_count(self, tmp_path: Path):
        self._populate_export_dir(tmp_path)
        runner.invoke(app, ["schema", "generate", str(tmp_path)])
        data = json.loads((tmp_path / ".dtr-schema.json").read_text())
        md_entries = data["markdown_structure"]
        assert len(md_entries) == 1
        assert md_entries[0]["heading_count"] >= 4

    def test_custom_output_path(self, tmp_path: Path):
        self._populate_export_dir(tmp_path)
        out = tmp_path / "custom-schema.json"

        result = runner.invoke(
            app, ["schema", "generate", str(tmp_path), "--output", str(out)]
        )

        assert result.exit_code == 0, result.stdout
        assert out.exists(), "Custom output file was not created"

    def test_pretty_flag_produces_indented_json(self, tmp_path: Path):
        self._populate_export_dir(tmp_path)
        runner.invoke(app, ["schema", "generate", str(tmp_path), "--pretty"])
        raw = (tmp_path / ".dtr-schema.json").read_text(encoding="utf-8")
        # Indented JSON has newlines
        assert "\n" in raw

    def test_no_pretty_flag_produces_compact_json(self, tmp_path: Path):
        self._populate_export_dir(tmp_path)
        out = tmp_path / "compact.json"
        runner.invoke(
            app,
            ["schema", "generate", str(tmp_path), "--no-pretty", "--output", str(out)],
        )
        raw = out.read_text(encoding="utf-8")
        # Compact JSON has no indentation (no leading spaces)
        assert "  " not in raw

    def test_generate_succeeds_on_empty_directory(self, tmp_path: Path):
        result = runner.invoke(app, ["schema", "generate", str(tmp_path)])

        assert result.exit_code == 0, result.stdout
        schema_file = tmp_path / ".dtr-schema.json"
        assert schema_file.exists()
        data = json.loads(schema_file.read_text())
        assert data["file_counts"]["html"] == 0
        assert data["file_counts"]["json"] == 0
        assert data["file_counts"]["markdown"] == 0

    def test_generate_excludes_existing_schema_file_from_json_analysis(self, tmp_path: Path):
        # Pre-populate a .dtr-schema.json so it would be found as a .json file
        (tmp_path / ".dtr-schema.json").write_text(
            json.dumps({"$schema": "previous"}), encoding="utf-8"
        )
        (tmp_path / "data.json").write_text(GOOD_JSON, encoding="utf-8")

        result = runner.invoke(app, ["schema", "generate", str(tmp_path)])

        assert result.exit_code == 0, result.stdout
        data = json.loads((tmp_path / ".dtr-schema.json").read_text())
        # Only data.json should be counted, not .dtr-schema.json
        assert data["file_counts"]["json"] == 1

