    @Test
    void a1_overview() {
        sayNextSection("OpenAPI REST API Documentation with DTR");

        say(
            "DTR transforms JUnit 5 tests into living API documentation. " +
            "Each `say*` call is a typed documentation event captured by the " +
            "RenderMachine and written to Markdown, LaTeX, HTML, and JSON outputs. " +
            "REST API docs written this way can never drift from the code because " +
            "the documentation is generated by running the code."
        );

        say("The four standard HTTP methods covered in this guide:");

        sayTable(new String[][] {
            {"Method",   "Semantics",          "Safe", "Idempotent", "Typical Status Codes"},
            {"GET",      "Retrieve resource",  "Yes",  "Yes",        "200, 304, 404"},
            {"POST",     "Create resource",    "No",   "No",         "201, 400, 409, 422"},
            {"PUT",      "Replace resource",   "No",   "Yes",        "200, 204, 404, 422"},
            {"DELETE",   "Remove resource",    "No",   "Yes",        "204, 404"},
        });

        sayNote(
            "Safe methods do not modify server state. Idempotent methods produce " +
            "the same result regardless of how many times they are called with the same input."
        );

        say("Request/response lifecycle for a typical REST call:");

        sayMermaid("""
                sequenceDiagram
                    participant Client
                    participant Gateway as API Gateway
                    participant Auth   as Auth Service
                    participant API    as Resource API
                    participant DB     as Database

                    Client->>Gateway: HTTP Request + Bearer token
                    Gateway->>Auth: Validate JWT
                    Auth-->>Gateway: 200 OK (claims)
                    Gateway->>API: Forward request + claims
                    API->>DB: Query / mutation
                    DB-->>API: Result set
                    API-->>Gateway: 200 OK (JSON body)
                    Gateway-->>Client: Response + CORS headers
                """);

        sayWarning(
            "All endpoints require a valid JWT Bearer token in the " +
            "`Authorization` header. Requests without a token receive " +
            "HTTP 401 Unauthorized immediately at the gateway."
        );
    }

