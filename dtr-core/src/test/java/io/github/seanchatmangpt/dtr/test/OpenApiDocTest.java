/**
 * 80/20 Blue Ocean Innovation: OpenAPI REST API Documentation.
 *
 * <p>Demonstrates how DTR's {@code say*} methods produce living, executable
 * OpenAPI-style documentation for REST APIs. Each test method documents one
 * aspect of the API lifecycle — overview, endpoint schema, error catalogue,
 * authentication, and versioned evolution — entirely through real Java code
 * with no external REST libraries or mocked HTTP calls.</p>
 *
 * <p>All structural data (endpoint tables, error codes, JWT claims) is
 * defined inline so it can be read, validated, and extended by any future
 * test run. Documentation cannot drift from the schema because the schema
 * IS the documentation.</p>
 */
