    @Test
    void a2_dtr_record_taxonomy() {
        sayNextSection("DTR's Record Types: A Taxonomy");

        say("DTR uses the sealed interface `SayEvent` as its core event hierarchy. "
                + "Every `say*` call on a `RenderMachine` produces a `SayEvent` record. "
                + "Using `getPermittedSubclasses()` (Java 17+) we can enumerate all permitted "
                + "subtypes at runtime and filter for those that are also records — "
                + "giving us a live, drift-proof taxonomy of every documentation event type.");

        Class<?>[] permitted = SayEvent.class.getPermittedSubclasses();

        List<String[]> rows = new ArrayList<>();
        rows.add(new String[]{"Record Type", "Package", "Component Count", "Components"});

        for (Class<?> c : permitted) {
            if (c.isRecord()) {
                String simpleName = c.getSimpleName();
                String pkg = c.getPackageName();
                int count = c.getRecordComponents().length;
                String components = Arrays.stream(c.getRecordComponents())
                        .map(RecordComponent::getName)
                        .collect(Collectors.joining(", "));
                rows.add(new String[]{simpleName, pkg, String.valueOf(count), components});
            }
        }

        sayTable(rows.toArray(new String[0][]));

        long recordCount = Arrays.stream(permitted).filter(Class::isRecord).count();
        say("DTR's `SayEvent` sealed interface has **" + permitted.length + "** permitted subtypes, "
                + "of which **" + recordCount + "** are records. "
                + "The remaining permitted types (if any) use classes or interfaces. "
                + "This table is generated entirely from reflection — no manual maintenance required.");
    }

