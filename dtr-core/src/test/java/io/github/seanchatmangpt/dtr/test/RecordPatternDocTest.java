    @Test
    void a1_records_overview() {
        sayNextSection("Records in Java 21+: Immutable Data Carriers");

        say("Java 16 introduced records as a concise syntax for declaring transparent, "
                + "immutable data carrier classes. A record declaration automatically "
                + "synthesises a canonical constructor, accessor methods (one per component), "
                + "and value-based implementations of `equals`, `hashCode`, and `toString`. "
                + "Java 21 extended this foundation with record patterns, allowing records to "
                + "participate directly in `instanceof` checks and `switch` expressions via "
                + "deconstruction.");

        say("**Automatic methods generated by the compiler for every record:**");
        sayUnorderedList(List.of(
                "`T componentName()` — accessor for each declared component",
                "`boolean equals(Object)` — structural equality by component values",
                "`int hashCode()` — consistent with `equals`, derived from components",
                "`String toString()` — human-readable representation of all components",
                "Canonical constructor `R(T1 c1, T2 c2, ...)` — validated at construction time"
        ));

        say("The one-line record declaration below replaces over 50 lines of equivalent class boilerplate:");

        sayCode("""
                // Record declaration — 1 line
                record Point(int x, int y) {}

                // Equivalent class — 50+ lines
                public final class Point {
                    private final int x;
                    private final int y;

                    public Point(int x, int y) {
                        this.x = x;
                        this.y = y;
                    }

                    public int x()       { return x; }
                    public int y()       { return y; }

                    @Override
                    public boolean equals(Object o) {
                        if (this == o) return true;
                        if (!(o instanceof Point p)) return false;
                        return x == p.x && y == p.y;
                    }

                    @Override
                    public int hashCode() { return Objects.hash(x, y); }

                    @Override
                    public String toString() {
                        return "Point[x=" + x + ", y=" + y + "]";
                    }
                }
                """, "java");

        sayNote("Records are implicitly `final` — they cannot be extended. "
                + "Components are implicitly `private final`. "
                + "This immutability guarantee is enforced by the compiler.");
    }

