    // a5 — Best practices
    // =========================================================================

    @Test
    void a5_best_practices() {
        sayNextSection("Best Practices for Git-Backed Documentation");

        say("These practices are derived from production experience integrating " +
                "`sayEvolutionTimeline` into CI pipelines. They prioritise " +
                "correctness and graceful degradation over completeness.");

        sayOrderedList(List.of(
                "Always pass a `maxEntries` limit to `sayEvolutionTimeline`. " +
                        "A class with 500 commits generates a 500-row table; " +
                        "5-10 entries is enough for a documentation audience.",
                "Treat empty timelines as informative, not as failures. " +
                        "A new class with zero history is worth documenting: " +
                        "it signals that no production incident has forced a change yet.",
                "Combine `sayEvolutionTimeline` with `sayCallSite` so readers " +
                        "know exactly which test method produced the timeline " +
                        "and when. Provenance is part of documentation quality.",
                "Run git-backed tests in CI with a full clone (`fetch-depth: 0` " +
                        "in GitHub Actions). Shallow clones truncate history and " +
                        "produce misleading commit counts.",
                "Pair `sayEvolutionTimeline` with `sayAsciiChart` to give " +
                        "decision-makers a single-screen hotspot view without " +
                        "requiring them to read commit tables.",
                "Use `ProcessBuilder` rather than `Runtime.exec(String)` for all " +
                        "git sub-processes. The array form prevents shell injection " +
                        "and makes argument lists auditable.",
                "Catch both `IOException` (process spawn failure) and non-zero " +
                        "exit codes separately. Exit code 128 from git usually means " +
                        "the working directory is not inside a repository."
        ));

        sayNote("`sayEvolutionTimeline` only works inside a git repository. " +
                "When the test runs outside a git context — for example, " +
                "in a Docker build stage that copies only compiled classes — " +
                "the method falls back to a NOTE block explaining that git " +
                "is unavailable, and the test still passes.");

        sayWarning("Shallow clones (`git clone --depth=N`) truncate commit history. " +
                "If your CI pipeline uses a shallow clone, `sayEvolutionTimeline` " +
                "will show fewer entries than exist in the full repository. " +
                "For accurate hotspot analysis, configure your CI checkout action " +
                "with `fetch-depth: 0` to retrieve the complete history.");

        say("Example GitHub Actions configuration for full history:");
        sayCode("""
                - uses: actions/checkout@v4
                  with:
                    fetch-depth: 0   # required for sayEvolutionTimeline
                """, "yaml");

        sayEnvProfile();
    }

    // =========================================================================
    // Private helpers
    // =========================================================================

    /**
     * Runs a git command via {@link ProcessBuilder}, applies {@code transform} to
     * the output lines, and returns the result. Returns {@code fallback} on any
     * {@link IOException} or non-zero exit code so tests never fail due to git
     * unavailability.
     *
     * @param command   the git command and arguments
     * @param transform maps the captured output lines to a String result
     * @param fallback  value returned when git is unavailable or exits non-zero
     * @return the transformed result or the fallback value
     */
    private String runGitCommand(
            List<String> command,
            java.util.function.Function<List<String>, String> transform,
            String fallback) {

        try {
            var pb = new ProcessBuilder(command).redirectErrorStream(true);
            var proc = pb.start();
            List<String> lines;
            try (var reader = new BufferedReader(
                    new InputStreamReader(proc.getInputStream()))) {
                lines = reader.lines().toList();
            }
            int exitCode = proc.waitFor();
            if (exitCode != 0) {
                return fallback;
            }
            return transform.apply(lines);
        } catch (IOException | InterruptedException e) {
            Thread.currentThread().interrupt();
            return fallback;
        }
    }
