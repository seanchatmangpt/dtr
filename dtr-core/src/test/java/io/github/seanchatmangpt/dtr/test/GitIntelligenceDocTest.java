    // =========================================================================
    // a3 — Repository-level profile via ProcessBuilder
    // =========================================================================

    @Test
    void a3_repository_profile() {
        sayNextSection("Repository Profile: Live Git Statistics");

        say("Beyond per-file history, DTR tests can query repository-level metrics " +
                "by spawning git sub-processes with `ProcessBuilder`. The data below " +
                "is read from the actual repository at test execution time.");

        String commitCount = runGitCommand(
                List.of("git", "log", "--oneline"),
                lines -> String.valueOf(lines.size()),
                "unavailable (git not found)");

        String contributorCount = runGitCommand(
                List.of("git", "shortlog", "-s", "--no-merges"),
                lines -> String.valueOf(lines.size()),
                "unavailable (git not found)");

        String currentBranch = runGitCommand(
                List.of("git", "rev-parse", "--abbrev-ref", "HEAD"),
                lines -> lines.isEmpty() ? "unknown" : lines.get(0).trim(),
                "unavailable (git not found)");

        String lastModified = runGitCommand(
                List.of("git", "log", "-1", "--pretty=format:%as"),
                lines -> lines.isEmpty() ? "unknown" : lines.get(0).trim(),
                "unavailable (git not found)");

        var stats = new LinkedHashMap<String, String>();
        stats.put("Total Commits", commitCount);
        stats.put("Contributors", contributorCount);
        stats.put("Branch", currentBranch);
        stats.put("Last Modified", lastModified);
        sayKeyValue(stats);

        sayCode("""
                // ProcessBuilder pattern for git commands in DTR tests
                var pb = new ProcessBuilder("git", "log", "--oneline")
                        .redirectErrorStream(true);
                var proc = pb.start();
                try (var reader = new BufferedReader(
                        new InputStreamReader(proc.getInputStream()))) {
                    long commitCount = reader.lines().count();
                }
                proc.waitFor();
                """, "java");

        sayNote("All git commands are executed via `ProcessBuilder`, not `Runtime.exec()`. " +
                "`ProcessBuilder` gives explicit control over the argument list (no shell " +
                "injection), stderr redirection, and working directory.");
    }

    // =========================================================================
    // a4 — Hotspot analysis across multiple classes
    // =========================================================================

    @Test
    void a4_hotspot_analysis() {
        sayNextSection("Hotspot Analysis: Which Classes Change Most Often?");

        say("A hotspot is a class that accumulates commits faster than its neighbours. " +
                "High commit frequency signals either active feature development or " +
                "chronic instability — context determines which. The chart below " +
                "derives commit counts from the real evolution timelines of three " +
                "core DTR classes, then renders the comparison as an ASCII bar chart.");

        // DtrTest — always present in the repo
        int dtrTestCommits = GitHistoryReader.read(DtrTest.class, 100).size();

        // RenderMachineImpl — another core class with expected history
        int renderMachineImplCommits = 0;
        try {
            renderMachineImplCommits = GitHistoryReader.read(RenderMachineImpl.class, 100).size();
        } catch (Exception e) {
            // class exists but may have no history in a shallow clone
        }

        // GitHistoryReader itself — likely newer, fewer commits
        int gitHistoryReaderCommits = 0;
        try {
            gitHistoryReaderCommits = GitHistoryReader.read(GitHistoryReader.class, 100).size();
        } catch (Exception e) {
            // graceful fallback
        }

        say("Evolution timelines for the three tracked classes:");
        sayEvolutionTimeline(DtrTest.class, 5);

        say("**RenderMachineImpl** recent history:");
        sayEvolutionTimeline(RenderMachineImpl.class, 5);

        say("Commit frequency comparison (derived from real timeline data, capped at 100 entries):");

        // Use at least 1 for each so the chart renders even in shallow clones
        double[] commitCounts = {
                Math.max(dtrTestCommits, 1),
                Math.max(renderMachineImplCommits, 1),
                Math.max(gitHistoryReaderCommits, 1),
        };
        String[] classLabels = {
                "DtrTest",
                "RenderMachineImpl",
                "GitHistoryReader",
        };
        sayAsciiChart("Commit Frequency (last 100 entries)", commitCounts, classLabels);

        sayTable(new String[][] {
                {"Class", "Commits (sampled)", "Interpretation"},
                {"DtrTest", String.valueOf(dtrTestCommits),
                        dtrTestCommits > 10 ? "Actively maintained base class" : "Stable — few recent changes"},
                {"RenderMachineImpl", String.valueOf(renderMachineImplCommits),
                        renderMachineImplCommits > 10 ? "High churn — core output engine" : "Stable implementation"},
                {"GitHistoryReader", String.valueOf(gitHistoryReaderCommits),
                        gitHistoryReaderCommits > 5 ? "Evolving git integration" : "New or stable utility"},
        });

        sayNote("Counts reflect commits visible in the current clone. Shallow clones " +
                "(depth=1 or depth=50) will show lower numbers than a full clone. " +
                "The relative proportions remain meaningful for hotspot identification.");
    }

