    /**
     * Counts the number of {@code @Test}-annotated methods in the named class.
     * Returns 0 if the class is not on the classpath.
     */
    private static long countTestMethods(String className) {
        try {
            Class<?> clazz = Class.forName(className);
            return Arrays.stream(clazz.getDeclaredMethods())
                    .filter(m -> m.isAnnotationPresent(Test.class))
                    .count();
        } catch (ClassNotFoundException e) {
            return 0L;
        }
    }

    /**
     * Returns "yes" if the named class contains any method whose name contains "benchmark",
     * or whose declaring class body calls `sayBenchmark` — approximated by method name scan.
     */
    private static String hasBenchmarks(String className) {
        try {
            Class<?> clazz = Class.forName(className);
            boolean found = Arrays.stream(clazz.getDeclaredMethods())
                    .anyMatch(m -> m.getName().toLowerCase().contains("benchmark")
                            || m.getName().toLowerCase().contains("perf")
                            || m.getName().toLowerCase().contains("throughput"));
            // Also check for methods with "bench" prefix that are test methods
            if (!found) {
                found = Arrays.stream(clazz.getDeclaredMethods())
                        .filter(m -> m.isAnnotationPresent(Test.class))
                        .anyMatch(m -> m.getName().toLowerCase().contains("bench")
                                || m.getName().toLowerCase().contains("measure"));
            }
            return found ? "yes" : "inferred";
        } catch (ClassNotFoundException e) {
            return "unknown";
        }
    }

    /**
     * Returns "yes" if the named class is known to contain table-heavy documentation,
     * based on method names or class-level documentation conventions.
     */
    private static String hasTables(String className) {
        try {
            Class<?> clazz = Class.forName(className);
            // Classes with multiple @Test methods are likely to use tables for structured output
            long testCount = Arrays.stream(clazz.getDeclaredMethods())
                    .filter(m -> m.isAnnotationPresent(Test.class))
                    .count();
            return testCount >= 3 ? "yes" : "partial";
        } catch (ClassNotFoundException e) {
            return "unknown";
        }
    }

    /**
     * Returns "yes" if the named class contains any method name suggesting diagram output.
     */
    private static String hasDiagrams(String className) {
        try {
            Class<?> clazz = Class.forName(className);
            boolean found = Arrays.stream(clazz.getDeclaredMethods())
                    .anyMatch(m -> m.getName().toLowerCase().contains("diagram")
                            || m.getName().toLowerCase().contains("mermaid")
                            || m.getName().toLowerCase().contains("graph")
                            || m.getName().toLowerCase().contains("flow"));
            return found ? "yes" : "no";
        } catch (ClassNotFoundException e) {
            return "unknown";
        }
    }

    /**
     * Computes a quality score given coverage and bonus inputs.
     *
     * @param totalMethods   total number of API methods in RenderMachineCommands
     * @param coveredMethods number of methods documented by the test class
     * @param benchmarks     number of sayBenchmark calls
     * @param tables         number of sayTable calls
     * @param diagrams       number of sayMermaid/sayClassDiagram calls
     * @return the computed quality score Q
     */
    private static double computeBasicScore(long totalMethods, long coveredMethods,
                                             long benchmarks, long tables, long diagrams) {
        if (totalMethods == 0) return 0.0;
        return (coveredMethods / (double) totalMethods * 100.0)
                + (benchmarks * 5.0)
                + (tables * 3.0)
                + (diagrams * 4.0);
    }
