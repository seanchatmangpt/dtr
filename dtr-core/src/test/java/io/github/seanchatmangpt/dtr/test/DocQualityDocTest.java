    // =========================================================================
    // Section 1: Documentation Quality — What It Means
    // =========================================================================

    @Test
    void a1_quality_overview() {
        sayNextSection("Documentation Quality: What It Means");

        say("Documentation quality is not a subjective judgment — it is a measurable property. " +
                "The four dimensions that determine whether documentation is trustworthy are: " +
                "coverage (which API surface is explained), depth (how thoroughly each item is explained), " +
                "examples (whether real, executable code accompanies every claim), " +
                "and accuracy (whether the documentation matches the actual behavior).");

        say("Traditional documentation fails on accuracy. Prose written alongside code diverges " +
                "the moment the code changes. README files rot. Javadoc falls out of sync with " +
                "method signatures. There is no enforcement mechanism.");

        say("DTR uniquely can measure all four dimensions because documentation IS tests. " +
                "Every `say*` call is a documentation primitive that runs during test execution. " +
                "If the test passes, the documentation is accurate — by construction. " +
                "If the benchmark runs, the performance claim is proven — not estimated. " +
                "If the assertion fires, the behavioral claim is verified — not assumed.");

        sayCode("""
                // Coverage: which methods are documented?
                sayDocCoverage(RenderMachineCommands.class);

                // Depth: how many say* primitives per section?
                // Measured by counting SayEvent emissions per test method.

                // Examples: are they real?
                sayBenchmark("label", () -> actualOperation()); // real measurement

                // Accuracy: does it match behavior?
                sayAndAssertThat("result", actual, is(expected)); // proven at runtime
                """, "java");

        sayNote("DTR is the only testing framework where the definition of 'accurate documentation' " +
                "is identical to the definition of 'passing tests'.");
    }

