    // Section 5: Documentation Anti-Patterns
    // =========================================================================

    @Test
    void a5_anti_patterns() {
        sayNextSection("Documentation Anti-Patterns");

        say("Documentation anti-patterns are practices that produce documentation which appears " +
                "complete but is unreliable, unverifiable, or actively misleading. Each anti-pattern " +
                "below represents a real failure mode that DTR prevents by construction.");

        sayTable(new String[][] {
                {"Anti-Pattern", "Why Bad", "DTR Prevention"},
                {"Stale docs",
                        "Prose written months ago no longer matches current behavior. " +
                                "No one notices until a user files a bug.",
                        "DTR docs are test output. They regenerate on every `mvnd verify`. " +
                                "Stale docs are impossible: the test would fail first."},
                {"Untested examples",
                        "Code snippets in README or Javadoc are copy-pasted and never compiled. " +
                                "They contain bugs, use deprecated APIs, or simply do not run.",
                        "`sayCode()` renders examples that live inside `@Test` methods. " +
                                "The surrounding test verifies that the code pattern is valid."},
                {"Missing benchmarks",
                        "Performance claims like 'this is fast' or '10x faster' appear in docs " +
                                "with no measurement methodology, no units, no iteration count.",
                        "`sayBenchmark()` refuses to document performance without real " +
                                "`System.nanoTime()` measurements, warmup rounds, and units."},
                {"No diagrams",
                        "Architecture and relationships described in prose are ambiguous. " +
                                "Readers build different mental models from the same text.",
                        "`sayMermaid()` and `sayClassDiagram()` generate diagrams from live " +
                                "class structure. The diagram IS the class — it cannot be wrong."},
                {"No assertions",
                        "Documentation claims behavior without proving it. " +
                                "'Returns null if not found' cannot be verified from docs alone.",
                        "`sayAssertions()` and `sayAndAssertThat()` document AND verify. " +
                                "If the assertion fails, the documentation is never generated."},
                {"Stub implementations",
                        "Test code calls `doSomething()` where the implementation is a no-op stub. " +
                                "The doc says 'proven' but the proof covers nothing real.",
                        "DTR's CLAUDE.md invariant: real code only. No stubs. " +
                                "Benchmarks measure real operations. Tables contain real measurements."},
        });

        sayWarning("The most dangerous anti-pattern is 'documentation that looks verified but is not.' " +
                "A README with code blocks feels authoritative. A test that calls stub methods feels rigorous. " +
                "DTR's defense is structural: the say* API cannot be called outside a running test.");
    }

